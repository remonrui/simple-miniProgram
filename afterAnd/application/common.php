/*
 * 获取月份
 * @param str $time  时间格式
 */
function getMonth($time)
{
    $month = substr($time, 5);
    $map = array('0', '1');
    if (in_array(substr($month, 0, 1), $map)) {
        //去掉前导零
        if (substr($month, 0, 1) == 0) {
            return substr($month, 1, 1);
        } else {
            return substr($month, 0, 2);
        }
    } else {
        return substr($month, 0, 1);
    }
}

/**
 * @param $month
 * @return array|string
 */
function getMonthTimestamp($month)
{
    if (!$month){
        return '';
    }
    $month_start = strtotime($month);//指定月份月初时间戳
    $month_end = mktime(23, 59, 59, date('m', strtotime($month))+1, 00);//指定月份月末时间戳
    return [$month_start,$month_end];
}

/*
 * 获取日
 */
function getDay($time)
{
    $num1 = strrpos($time, '.');
    if ($num1 === false) {
        $num2 = strrpos($time, '-');
        return substr($time, $num2 + 1);
    } elseif (substr($time, $num1 + 1) != '' && $num1 > 4) {
        return substr($time, $num1 + 1);
    }
}

/**
 * 把返回的数据集转换成Tree
 * @param array $list 要转换的数据集
 * @param string $pid parent标记字段
 * @param string $level level标记字段
 * @return array
 */
function list_to_tree($list, $pk='id', $pid = 'pid', $child = '_child', $root = 0) {
    // 创建Tree
    $tree = array();
    if(is_array($list)) {
        // 创建基于主键的数组引用
        $refer = array();
        foreach ($list as $key => $data) {
            $refer[$data[$pk]] =& $list[$key];
        }
        foreach ($list as $key => $data) {
            // 判断是否存在parent
            $parentId =  $data[$pid];
            if ($root == $parentId) {
                $tree[] =& $list[$key];
            }else{
                if (isset($refer[$parentId])) {
                    $parent =& $refer[$parentId];
                    $parent[$child][] =& $list[$key];
                }
            }
        }
    }
    return $tree;
}

/**
 * 将list_to_tree的树还原成列表
 * @param  array $tree  原来的树
 * @param  string $child 孩子节点的键
 * @param  string $order 排序显示的键，一般是主键 升序排列
 * @param  array  $list  过渡用的中间数组，
 * @return array        返回排过序的列表数组
 */
function tree_to_list($tree, $child = '_child', $order='id', &$list = array()){
    if(is_array($tree)) {
        foreach ($tree as $key => $value) {
            $reffer = $value;
            if(isset($reffer[$child])){
                unset($reffer[$child]);
                tree_to_list($value[$child], $child, $order, $list);
            }
            $list[] = $reffer;
        }
        $list = list_sort_by($list, $order, $sortby='asc');
    }
    return $list;
}